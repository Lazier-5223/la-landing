function gradeFromNicheRank(nicheRank: string): string {
  const m = nicheRank.match(/\(([^)]+)\)/);
  return (m?.[1] ?? "—").trim();
}

export const districtsData: DistrictData[] = rawDistrictsData.map((d) => ({
  ...d,
  hardMetrics: {
    grade: d.hardMetrics?.grade ?? gradeFromNicheRank(d.nicheRank),
  },
}));

/** 解析「中文 (English)」展示名 */
export function splitDistrictName(name: string): { zh: string; en: string } {
  const m = name.match(/^(.+?)\s*\((.+)\)\s*$/);
  if (!m) return { zh: name.trim(), en: "" };
  return { zh: m[1].trim(), en: m[2].trim() };
}

export function getDistrictById(id: string): DistrictData | undefined {
  return districtsData.find((d) => d.id === id);
}
